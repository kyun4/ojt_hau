            <h4 style="letter-spacing: 5px;text-transform:uppercase"><center>WITNESSETH: </center></h4>
            <p align="justify" style="text-indent: 15px;">
                <strong>WHEREAS</strong>, the University acknowledges that one of the means of achieving its Mission is to develop the moral and academic growth of its students, and to expose them to commerce best-practices and realities in various fields;
            </p>
            <p align="justify" style="text-indent: 15px;">
                <strong>WHEREAS</strong>, the University, in the quest of its objectives, aims to mold responsible, efficient and flexible individuals through intellectual trainings in diversified lines of work.
            </p>
            <p align="justify" style="text-indent: 15px;">
                <strong>WHEREAS</strong>, the Company concedes the expressed need of the University to assign its students to actual tasks (virtually or on-site depending on the situation) for the purpose of supplementing their acquired higher learning, and is prepared to help the students in this undertaking.
            </p>
            <p align="justify" style="text-indent: 15px;">   
                <strong>WHEREAS</strong>, the Company shall provide the students with learning experiences, in preparation for the actual application of the students’ knowledge based on their chosen courses after graduation. 
            </p>
            <p align="justify" style="text-indent: 15px;">
                <strong>NOW THERFORE</strong>, for and in consideration of the above-mentioned premises, the parties hereto mutually agree as follows: 
            
            </p>
            <ol type="I">
                <li><strong>Roles and Responsibilities:</strong><br /><br />
                    <ol type="A">
                        <li><strong>Company</strong><br /><br />
                            <ol type="1">
                                <li>The Company should develop the Organization OJT Program with specific goals and objectives, and which the UNIVERSITY finds to be complementary to its University OJT Program<br /><br /></li>
                                <li>Accept students for OJT (on-the-job-training) endorsed by the University in its offices/ departments as it may deem necessary and/or appropriate based on the student’s qualification and field of specialization.<br /><br /></li>
                                <li>Observe due care and diligence to student-trainee in work situations and activities where higher degree of risks are anticipated.<br /><br /></li>
                                <li>Protect students against unacceptable conduct of sexual nature and cruel or inhuman treatment by the Company staff<br /><br /></li>
                                <li>Designate a coordinator to work with the University<br /><br /></li>
                                <li>Provide students with variety of work assignments virtually or on-site depending on the situation, suitable to their chosen courses to be supervised by an experienced staff<br /><br /></li>
                                <li>Make a periodic evaluation of job progress based on the rating/criteria form provided by the University<br /><br /></li>
                                <li>Award the student-trainee an OJT (on-the-job training) Completion Certificate at the end of the specified training period.<br /><br /></li>
                                <li>Shall not designate/assign the student-trainee in any hazardous work or would put them in harm.<br /><br /></li>
                                <li>1No employer and employee relationship shall exist between the student-trainee and the company.<br /><br /></li>
                            </ol>
                        </li>
                        <li><strong>University</strong><br /><br />
                            <ol type="1">
                                <li>Designate a practicum coordinator that will work with the  representative of the Company.<br /><br /></li>
                                <li>Endorse qualified students for OJT program for which a list shall be submitted to the Company.<br /><br /></li>
                                <li>Offer necessary guidance and/or assistance to both student-trainees and representative of the Company<br /><br /></li>
                                <li>Provide for the coverage of accident and death insurance during the OJT period and within the Company premises or during work done in relation to the OJT Training Program.<br /><br /></li>
                                <li>Monitor progress and assist in evaluation of student <br /><br /></li>
                                <li>Establish high standards and expectations for the student <br /><br /></li>
                                <li>Arrange the signing by the student any document required by the COMPANY.<br /><br /></li>
                            </ol>
                        </li>
                        <li><strong>Student- Trainee</strong><br /><br />
                            <ol type="1">
                                <li>Inform his/her parents or guardian and University authorities his/her intention to undergo OJT as certified by the Parental Waiver and Practicum Coordinator’s endorsement.<br /><br /></li>
                                <li>Agree to perform the assigned duties and other lawful directions given by his/her supervisor.<br /><br /></li>
                                <li>Maintain regular attendance on the job and notify his/her supervisor prior to any absence.<br /><br /></li>
                                <li>Show punctuality, honesty, a cooperative attitude, proper health and grooming practice, and willingness to learn.<br /><br /></li>
                                <li>Shall not divulge to any person, firm or company any information concerning the affairs of the Company, which he/she may have acquired by reason of this training.<br /><br /></li>
                                <li>Conduct himself/herself in a manner as not to bring disgrace or dishonor to himself/herself and to the University.<br /><br /></li>
                                <li>Consult his/her supervisor as well as his/her practicum coordinator about any problem that may arise.<br /><br /></li>
                            </ol>
                        </li>
                    </ol>
                </li>
                <li><strong>Procedures</strong>
                    <p>The Company and the University shall observe the following procedures:</p>
                    <ol type="1">
                        <li>The Practicum Coordinator shall endorse the qualified student trainee to the Company.<br /><br /></li>
                        <li>The Practicum Coordinator shall advise the student trainee to meet the HRD and submit the Practicum Training Agreement and Waiver<br /><br /></li>
                        <li>After verifying the submitted documents, the coordinator shall accept the application for OJT of the student and conduct an orientation to the student on rudiments of OJT (on-the-job training) Program.<br /><br /></li>
                        <li>The Practicum Coordinator shall introduce the student trainee to work mentor and supervisor after such training.<br /><br /></li>
                        <li>The supervisor shall give specific instruction to the student trainee on work norms and responsibilities.<br /><br /></li>
                        <li>The supervisor shall accomplish an evaluation of the student trainee at the end of his/her training period and submit the form to the Practicum Coordinator.<br /><br /></li>
                        <li>The  Company shall prepare the OJT (on-the-job training) Completion Certificate of the student apprentice for submission to the University.<br /><br /></li>
                    </ol>
                </li>
                <li><strong>Grounds for Withdrawal of the Student OJT (on-the-job training)</strong><br /><br />
                    <p>Both parties agree that students can be withdrawn from the OJT (on-the-job training) Program on the following grounds:</p>
                    <ol>
                        <li>Willful disobedience to the company rules and regulations.<br /><br /></li>
                        <li>Insubordination to lawful orders of the supervisor.<br /><br /></li>
                        <li>Violence or other forms of gross misconduct by the student while inside the Company premises.<br /><br /></li>
                        <li>Immoral, physical, sexual abuse or harassment of the student by the employer or any of its employees.<br /><br /></li>
                        <li>Poor performance on the job for a prolonged period despite warnings duly given to the student apprentice.<br /><br /></li>
                        <li>Habitual neglect of his/her assigned duties/tasks.<br /><br /></li>
                        <li>Dishonesty or misconduct.<br /><br /></li>
                        <li>Poor physical condition, permanent disability or prolonged illness which incapacitates the apprentice.<br /><br /></li>
                        <li>Theft or malicious destruction of company property and/or equipment.<br /><br /></li>
                        <li>Substandard or hazardous working conditions.<br /><br /></li>
                        <li>Repeated violations by the employer of the terms of the OJT (on-the-job training) agreement.<br /><br /></li>
                        <li>Cruel or inhuman treatment by the employer or his subordinates<br /><br /></li>
                        <li>Personal problems which in the opinion of the trainee shall prevent him/her a satisfactory performance of his/her job.<br /><br /></li>
                        <li>Non-enrollment or dropping out from the University<br /><br /></li>
                    </ol>
                </li>
                <li><strong>Term and Effectivity:</strong> <br /><br />
                    <p align='justify' style="text-indent: 15px">This Memorandum of Understanding shall be effective for a period of Three (3) school year beginning with the semester covering the date of signing and shall be renewed upon mutual agreement. Termination or Withdrawal from this agreement by one party can be done upon written notice to the other party of the former’s intention to do so within thirty (30) days preceding the academic year in which it plans to cease participation.</p>
                </li>
            </ol>
            <p align='justify' style="text-indent: 15px">
